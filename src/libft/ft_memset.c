#include "libft.h"

void	*ft_memset(void *str, char c, size_t n)
{
	void	*p1;
	int		i;

	p1 = str;
	i = 0;
	while (n > 0)
	{
		((char *)str)[i] = c;
		i++;
		n--;
	}
	return (p1);
}
